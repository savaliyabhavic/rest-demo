package com.ikhodal.empdeptrest.models;

public class DeptModel
{

    private int deptNo;
    private String deptName;
    private String deptLocaltion;


    public int getDeptNo()
    {
        return deptNo;
    }

    public void setDeptNo(int deptNo)
    {
        this.deptNo = deptNo;
    }

    public String getDeptName()
    {
        return deptName;
    }

    public void setDeptName(String deptName)
    {
        this.deptName = deptName;
    }

    public String getDeptLocaltion()
    {
        return deptLocaltion;
    }

    public void setDeptLocaltion(String deptLocaltion)
    {
        this.deptLocaltion = deptLocaltion;
    }
}
